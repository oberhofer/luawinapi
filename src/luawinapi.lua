function GET_XY_LPARAM(lParam)
  local x = LOWORD(lParam)
  if x > 32768 then
      x = x - 65536
  end
  local y = HIWORD(lParam)
  if y > 32768 then
      y = y - 65536
  end
  return x, y
end

function GET_POINT_LPARAM(lParam)
  local pt = winapi.POINT:new()
  pt.x, pt.y = GET_XY_LPARAM(lParam)
  return pt
end


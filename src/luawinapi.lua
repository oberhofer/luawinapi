function winapi.Assert(result)
  if ((nil == result) or (0 == result)) then
    local errcode = winapi.GetLastError()
    if (S_OK ~= errcode) then
      error("Windows Error : " .. errcode, 2)
    end
  end
end

function ValidHandle(value)
  winapi.Assert(value)
  return value
end


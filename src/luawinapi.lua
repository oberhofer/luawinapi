-- converts ASCII to UCS2
function toUCS2(text)
  local result = {}
  for idx = 1, string.len(text) do
    table.insert(result, string.sub(text, idx, idx) .. "\000")
  end
  return table.concat(result);
end

function toUCS2Z(text)
  local result = {}
  for idx = 1, string.len(text) do
    table.insert(result, string.sub(text, idx, idx) .. "\000")
  end
  table.insert(result, "\000\000")
  return table.concat(result);
end

-- for convenience
_T = toUCS2Z

---------------------------------------------------------------------
-- converts UCS2 to ASCII
function toASCII(text)
  local result = {}
  for idx = 1, string.len(text), 2 do
    table.insert(result, string.sub(text, idx, idx))
  end
  return table.concat(result);
end

---------------------------------------------------------------------
-- converts bool to BOOL
function toBOOL(value)
  if (value) then
    return TRUE
  else
    return FALSE
  end
end

---------------------------------------------------------------------
-- define GUIDs
function DEFINE_GUID(a, b, c, d1, d2, d3, d4, d4, d6, d7, d8)
  local res = winapi.GUID:new()
  res.Data1 = a
  res.Data2 = b
  res.Data3 = c
  res.Data4 = {d1, d2, d3, d4, d4, d6, d7, d8}
  return res
end

